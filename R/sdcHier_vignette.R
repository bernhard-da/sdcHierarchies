#' sdcHier_vignette
#'
#' starts the package vignette that gets you started with the package
#'
#' @return a browser windows/tab with showing the vignette
#' @export
#' @examples
#' \dontrun{
#' sdcHier_vignette()
#' }
sdcHier_vignette <- function() {
  RShowDoc("usage", package = "sdcHierarchies")
}
